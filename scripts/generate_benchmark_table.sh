#!/usr/bin/env bash

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
OUT_DIR="${ROOT_DIR}/target/benchmark-compare"
DOC_PATH="${ROOT_DIR}/docs/benchmark-results.md"

STEPS="${STEPS:-5000}"
ITERATIONS="${ITERATIONS:-30}"
WARMUP="${WARMUP:-5}"

mkdir -p "${OUT_DIR}"

MANUAL_TIMING_OUT="${OUT_DIR}/backend_timing_manual.txt"
NALGEBRA_TIMING_OUT="${OUT_DIR}/backend_timing_nalgebra.txt"
MANUAL_BENCH_OUT="${OUT_DIR}/criterion_ekf_manual.txt"
NALGEBRA_BENCH_OUT="${OUT_DIR}/criterion_ekf_nalgebra.txt"

run_backend_timing() {
    local mode="$1"
    local output="$2"

    if [[ "${mode}" == "manual" ]]; then
        cargo run --release --example backend_timing -- \
            --steps "${STEPS}" \
            --iterations "${ITERATIONS}" \
            --warmup "${WARMUP}" \
            > "${output}"
    else
        cargo run --release --features nalgebra-backend --example backend_timing -- \
            --steps "${STEPS}" \
            --iterations "${ITERATIONS}" \
            --warmup "${WARMUP}" \
            > "${output}"
    fi
}

run_criterion_ekf() {
    local mode="$1"
    local output="$2"

    if [[ "${mode}" == "manual" ]]; then
        cargo bench --bench backend_benchmark ekf -- --noplot > "${output}"
    else
        cargo bench --features nalgebra-backend --bench backend_benchmark ekf -- --noplot > "${output}"
    fi
}

extract_backend_timing_value() {
    local key="$1"
    local file="$2"
    grep "^${key}=" "${file}" | head -n 1 | cut -d'=' -f2
}

criterion_mid_value_us() {
    local backend="$1"
    local steps="$2"
    local file="$3"

    python3 - "$backend" "$steps" "$file" <<'PY'
import re
import sys

backend, steps, path = sys.argv[1:]
pattern = re.compile(rf"^ekf/{re.escape(backend)}/{re.escape(steps)}\s+time:\s+\[([0-9.]+)\s+(\S+)\s+([0-9.]+)\s+(\S+)\s+([0-9.]+)\s+(\S+)\]$")
scale = {"ns": 1e-3, "µs": 1.0, "us": 1.0, "ms": 1e3, "s": 1e6}

with open(path, "r", encoding="utf-8") as handle:
    for raw_line in handle:
        line = raw_line.rstrip("\n")
        match = pattern.match(line)
        if not match:
            continue
        mid_value = float(match.group(3))
        mid_unit = match.group(4)
        if mid_unit not in scale:
            raise SystemExit(f"unsupported unit: {mid_unit}")
        print(f"{mid_value * scale[mid_unit]:.3f}")
        raise SystemExit(0)

raise SystemExit(f"benchmark result not found for ekf/{backend}/{steps} in {path}")
PY
}

format_delta_pct() {
    local baseline="$1"
    local contender="$2"
    awk -v base="${baseline}" -v other="${contender}" 'BEGIN { printf "%.1f%%", ((other - base) / base) * 100.0 }'
}

faster_backend() {
    local manual="$1"
    local nalgebra="$2"
    awk -v manual="${manual}" -v nalgebra="${nalgebra}" 'BEGIN {
        if (manual < nalgebra) {
            printf "manual";
        } else if (nalgebra < manual) {
            printf "nalgebra";
        } else {
            printf "tie";
        }
    }'
}

run_backend_timing manual "${MANUAL_TIMING_OUT}"
run_backend_timing nalgebra "${NALGEBRA_TIMING_OUT}"
run_criterion_ekf manual "${MANUAL_BENCH_OUT}"
run_criterion_ekf nalgebra "${NALGEBRA_BENCH_OUT}"

manual_timing_ms="$(extract_backend_timing_value "mean_ms" "${MANUAL_TIMING_OUT}")"
manual_timing_ns_step="$(extract_backend_timing_value "mean_ns_per_step" "${MANUAL_TIMING_OUT}")"
nalgebra_timing_ms="$(extract_backend_timing_value "mean_ms" "${NALGEBRA_TIMING_OUT}")"
nalgebra_timing_ns_step="$(extract_backend_timing_value "mean_ns_per_step" "${NALGEBRA_TIMING_OUT}")"

manual_ekf_100_us="$(criterion_mid_value_us "manual" "100" "${MANUAL_BENCH_OUT}")"
manual_ekf_1000_us="$(criterion_mid_value_us "manual" "1000" "${MANUAL_BENCH_OUT}")"
manual_ekf_5000_us="$(criterion_mid_value_us "manual" "5000" "${MANUAL_BENCH_OUT}")"

nalgebra_ekf_100_us="$(criterion_mid_value_us "nalgebra" "100" "${NALGEBRA_BENCH_OUT}")"
nalgebra_ekf_1000_us="$(criterion_mid_value_us "nalgebra" "1000" "${NALGEBRA_BENCH_OUT}")"
nalgebra_ekf_5000_us="$(criterion_mid_value_us "nalgebra" "5000" "${NALGEBRA_BENCH_OUT}")"

cat > "${DOC_PATH}" <<EOF
# Benchmark Results

Generated by \`scripts/generate_benchmark_table.sh\`.

## Setup

- \`backend_timing\`: \`cargo run --release --example backend_timing\`
- \`criterion ekf\`: \`cargo bench --bench backend_benchmark ekf -- --noplot\`
- steps: \`${STEPS}\`
- iterations: \`${ITERATIONS}\`
- warmup: \`${WARMUP}\`

## Summary

| workload | manual | nalgebra | delta vs manual | faster |
| --- | ---: | ---: | ---: | --- |
| backend_timing | ${manual_timing_ms} ms (${manual_timing_ns_step} ns/step) | ${nalgebra_timing_ms} ms (${nalgebra_timing_ns_step} ns/step) | $(format_delta_pct "${manual_timing_ms}" "${nalgebra_timing_ms}") | $(faster_backend "${manual_timing_ms}" "${nalgebra_timing_ms}") |
| ekf/100 | ${manual_ekf_100_us} us | ${nalgebra_ekf_100_us} us | $(format_delta_pct "${manual_ekf_100_us}" "${nalgebra_ekf_100_us}") | $(faster_backend "${manual_ekf_100_us}" "${nalgebra_ekf_100_us}") |
| ekf/1000 | ${manual_ekf_1000_us} us | ${nalgebra_ekf_1000_us} us | $(format_delta_pct "${manual_ekf_1000_us}" "${nalgebra_ekf_1000_us}") | $(faster_backend "${manual_ekf_1000_us}" "${nalgebra_ekf_1000_us}") |
| ekf/5000 | ${manual_ekf_5000_us} us | ${nalgebra_ekf_5000_us} us | $(format_delta_pct "${manual_ekf_5000_us}" "${nalgebra_ekf_5000_us}") | $(faster_backend "${manual_ekf_5000_us}" "${nalgebra_ekf_5000_us}") |

## Raw Outputs

- manual timing: [target/benchmark-compare/backend_timing_manual.txt](${ROOT_DIR}/target/benchmark-compare/backend_timing_manual.txt)
- nalgebra timing: [target/benchmark-compare/backend_timing_nalgebra.txt](${ROOT_DIR}/target/benchmark-compare/backend_timing_nalgebra.txt)
- manual criterion: [target/benchmark-compare/criterion_ekf_manual.txt](${ROOT_DIR}/target/benchmark-compare/criterion_ekf_manual.txt)
- nalgebra criterion: [target/benchmark-compare/criterion_ekf_nalgebra.txt](${ROOT_DIR}/target/benchmark-compare/criterion_ekf_nalgebra.txt)
EOF

printf 'Wrote %s\n' "${DOC_PATH}"
